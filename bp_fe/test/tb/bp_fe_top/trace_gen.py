import numpy as np

instr_width_p = 32

class TraceGen:

  # constructor
  def __init__(self, vaddr_width_p, instr_width_p):
    self.msg_type_width_p = 4
    self.vaddr_width_p = vaddr_width_p
    self.instr_width_p = instr_width_p
    self.packet_len = self.msg_type_width_p + self.instr_width_p + self.vaddr_width_p

  # print header
  def print_header(self):
    header = "// generated by trace_gen.py \n"
    header = "// [ message type (4 bit) | instr (32 bit) | vaddr (39 bit) ]\n"
    header = "// message types: 0 = fetch\n"
    header += "// packet_len = " + str(self.packet_len) + "\n" 
    return header

  # receive fetch
  def recv_pc_instr(self, pc, instr):
    packet = "0010_"
    packet += "0000_"
    packet += format(instr, "0"+str(self.instr_width_p)+"b") + "_"
    packet += format(pc, "0"+str(self.vaddr_width_p)+"b") + "\n"
    return packet

  # wait for a number of cycles
  # num_cycles: number of cycles to wait.
  def wait(self, num_cycles):
    command = "0110_" + format(num_cycles, "0" + str(self.packet_len) + "b") + "\n"
    command += "0101_" + (self.packet_len)*"0" + "\n"
    return command

  # finish trace
  def test_finish(self):
    command = "// FINISH \n"
    command += self.wait(8)
    command += "0100_" + (self.packet_len)*"0" + "\n"
    return command

  def test_done(self):
    command = "// DONE \n"
    command += self.wait(8)
    command += "0011_" + (self.packet_len)*"0" + "\n"
    return command

  # wait for a single cycle
  def nop(self):
    return "0000_" + "0"*(self.packet_len) + "\n"

  def print_comment(self, comment):
    return "// " + comment + "\n"
